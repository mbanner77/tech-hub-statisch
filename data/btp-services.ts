// Generiere Bilder für BTP Services
export const generateBTPServiceImageUrl = (serviceId: string, service?: BTPService): string => {
  // Wenn der Service ein Icon hat, verwende es
  if (service?.icon) {
    return service.icon
  }

  // Versuche, ein Bild aus dem serviceId zu generieren
  const imagePath = `/images/btp-services/${serviceId}.png`

  // Wenn kein Service-Objekt übergeben wurde, gib einfach den Pfad zurück
  if (!service) {
    return imagePath
  }

  // Wenn ein Service-Objekt übergeben wurde, generiere ein Platzhalterbild als Fallback
  return imagePath
}
